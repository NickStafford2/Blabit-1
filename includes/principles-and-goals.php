<h2>Design Principles and Goals:</h2>
<h4>Prioritize content staying power</h4>
<h4>Prioritize reputation</h4>
<p>Use reputation as a means of heuristically predict the likelihood of a users claim</p>
<p>Avoid users ability to cheat the system</p>
<h4>Fight the natural human tendency of clickbait</h4>
<p>if users don't stay, but often click, content priority will be lowered</p>
<h4>Avoid ideological echo chambers</h4>
<h4>Algorithm to predict the ideology and culture of content</h4>
<p>rank according to left/right wing scale, and by spiciness of memes</p>
